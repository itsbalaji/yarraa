package com.yarraa.vo;

public class StatsVO {
	
	private int userCount;
	private int brandCount;
	private int serviceCentersCount;
	public int getUserCount() {
		return userCount;
	}
	public void setUserCount(int userCount) {
		this.userCount = userCount;
	}
	public int getBrandCount() {
		return brandCount;
	}
	public void setBrandCount(int brandCount) {
		this.brandCount = brandCount;
	}
	public int getServiceCentersCount() {
		return serviceCentersCount;
	}
	public void setServiceCentersCount(int serviceCentersCount) {
		this.serviceCentersCount = serviceCentersCount;
	}

}
